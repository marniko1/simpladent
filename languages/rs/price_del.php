                        <p>Simpladent-tretman Vam omogućava da ustedite novac. Razlog se ne ogleda u tome što Vam nudimo jeftine implantate i neoriginalan pribor za ugradnju. Omogućavamo Vam da uštedite novac, jer koristimo naprednu i efektivnu tehnologiju. U većini slučajeva naš profesionalan rad je 2 do 3 puta jeftiniji. Biće Vam potrebno manje dolazaka u stomatološku ordinaciju, manje preduzetih koraka tokom trajanja tretmana (nema potrebe za ugradnjom veštačke kosti) i medikamenti Vam gotovo neće biti potrebni. Vreme tretmana će se smanjiti dramatično.</p>
