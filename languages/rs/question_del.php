<h2>Kako se pokazala Simpladent® metoda u poređenju sa drugim tretmanima imedijantnog opterećenja, fiksiranih zuba u roku od tri dana ili „All-on-4“?</h2>

„All-on-fo-4“ metoda podrazumeva ugradnju četiri konvencionalna implantata u vilicu pod uglom. (Slično „All-on-6“ predstavlja šest implantata usidrenih pod uglom). Ova metoda nosi brojne rizike: zbog malog broja implantata i popuštanja šrafova, frakture implantata se mogu očekivati tokom vremena. Samo jedan frakturiran implantat zahteve zamenu cele konstrukcije. To dovodi do povećanja troškova i produžava vreme potrebno za tretman. Pošto mastikatorni centar nije zamenjen, korektno žvakanje postaje teško. Dodatno, rizik od periimplantitisa ostaje visok, pogotovo za rizične grupe, kao što su pušači i dijabetičari. 
<br />
Koncept Simpladent® tretmana zahteva ugradnju adekvatnog broja kortikalnih-bazalnih implantata u čvrste delove kosti za optimalnu stabilnost i dugotrajne rezultate. Oni su konstruisani za imedijantno opterećenje. 
