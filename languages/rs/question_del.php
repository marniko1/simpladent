	<li>U radu sa pacijentima koji imaju veliku količinu koštanog tkiva (vertikalno i horizontalno), dobri estetski rezultati mogu se postići svakom metodom. </li>
	<br />
	<li>Ako pacijent ima veoma malu količinu koštanog tkiva (konvencionalni tretman je nemoguć, ne savetuje se ili održiv jedino uz prethodne ekstenzivne i rizične procedure augmentacija kosti), Simpladent metoda i u ovom slučaju može da ponudi uspešan tretman u protokolu imedijantnog opterećenja. U pojedinim slučajevima su neophodni kompromisi u estetskim rešenjima, zbog veoma redukovane količine kosti i promenjenog položaja oralne sluzokože. U ovim slučajevima specijalisti mogu da sugerišu dodatne terapijske korake, individualno primenljive.
</li>
</ul>
