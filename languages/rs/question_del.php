Povremeno nailazimo na tvrdnje da su estetski rezultati naše metode lošiji od rezultata konvencionalnim tretmanom. Ne slažemo se sa tvrdnjom da neproporcionalno pomažemo pacijentima koji nisu kandidati za konvencionalni tretman. Zbog toga je nemoguće porediti ova dva pristupa tretmanu.
<br />
