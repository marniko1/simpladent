        <h1>Istorija</h1>
        <div class="article">
            <p><strong>Dr.IhdeDental</strong> je osnovao zubni tehničar Klaus Ihde 1954. godine u Berlinu. Od dana osnivanja kompanija se profilirala kao dobavljač visokokvalitetnog stomatološkog potrošnog materijala. Krajem osamdesetih godina kompanija se transformiše u <strong>Dr.IhdeDental</strong> grupu I započinje sa sopstvenom proizvodnjom.</p>
            <p>IhdeDental grupa je jedna od vodećih svetskih kompanija za proizvodnju implantata za rad u protokolu imedijantnog opterećenja. Danas se proizvodi ove kompanije koriste u više od 40 zemalja. Proizvedeno je više od 1 000 000 implantata u međuvremenu. Ranije proizvodnja nije bila organizovana u kompaniji, međutim danas se sve faze proizvodnje odvijaju u glavnoj kompaniji u Švacarskoj.</p>
            <p>IhdeDental je razvio implantate koje je moguće koristiti čak I u neobičnim slučajevima I situacijama gde nema dovoljno koštane strukture. To je transplantaciju kosti I postupak augmentacije učinilo nepotrebnim.</p>
            <p>Lider kompanije je Prof.Dr.Stefan Ihde, stomatolog & oralni hirurg, koji je stomatološko obrazovanje stekao u Zapadnoj Nemačkoj (1982-1987). On nije razvio samo implantate za modernu dentalnu implantologiju, već je definisao i kategorizovao metode  koji se koriste širom sveta od strane obučenih doktora za ovaj sistem. Pod njegovim vođstvom je osnovana Simpladent asocijacija dentalnih klinika iz celog sveta. </p>
