        <h2>Sta Vam možemo ponuditi?</h2>
        <p>Naš tretman koncipiran je tako da reši sve Vaše probleme odjednom, zube koji nedostaju, nesposobnost da normalno jedete I smejete se, periodontalne probleme (upale desni i mobilnost zuba), endodontske probleme (granulome I ciste), kao i probleme sa teško pokvarenim ili rekonstruisanim zubima.</p>
      
        <p>Obično pristupamo rešavanju svih problema u jednom koraku. Vađenje zuba I ugradnja implantata se obavljaju u jednoj poseti I odmah nakon toga naš labaratorijski tim počinje da radi na Vašim novim zubima. Kako bi se izbegli problemi u budućnosti, odstranjuju se takođe zubi sa bolom i zubi za koje postoji velika verovatnoća da bi mogli da uzrokuju probleme u periodu od narednih 5 godina, te se zamenjuju implantatima.</p>
        <p>Naše iskustvo stečeno hiljadama implantoloških operacija jasno pokazuje da su implantati kod odraslih pacijenata mnogo pouzdaniji od njihovih vlastitih zuba.</p>
